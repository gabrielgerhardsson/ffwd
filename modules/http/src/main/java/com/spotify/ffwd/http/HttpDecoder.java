@Slf4j
@Sharable
public class HttpDecoder extends MessageToMessageDecoder<HttpMessage> {
    public static final int MAX_FRAME_SIZE = 0xffffff;

    @Override
    protected void decode(ChannelHandlerContext ctx, HttpMessage in, List<Object> out)
        throws Exception {

        log.info("HttpMessage: {}", in);

        out.add(new DefaultFullHttpResponse(HttpVersion.HTTP_1_1, HttpResponseStatus.OK));
    }

}
